    private void btnSignUpMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSignUpMouseClicked
        String firstName = txtFirstname.getText();
        String lastName = txtLastname.getText();
        String middlename = "  ";
        String gender = (String)cbGender.getSelectedItem();
        String username = txtUsername.getText().toLowerCase(); //Get name from textbox and store in Variable
        String password = new String(txtPassword.getPassword()).toLowerCase();//Get password from pass-box and store in Variable
        
        if(firstName.equals("") || lastName.equals("") || gender.equals("") || username.equals("") || password.equals("")){
            JOptionPane.showMessageDialog(null, "Field Cannot Be Empty!");//null is use to make message-box at the middle of the screen
        }else{
            
            if(checkExistingUsername(username)){
                JOptionPane.showMessageDialog(null, "Username Already Exist! Please Enter New Username.");
            }else{
                String query = "INSERT INTO Account(firstname,lastname,gender,balance,username,password) values (?,?,?,?,?,?)";
                try {
                    PreparedStatement pst = SQLConnection.getConnection().prepareStatement(query);
                    pst.setString(1, firstName);
                    pst.setString(2, lastName);
                    pst.setString(3, gender);
                    pst.setString(4, "0");
                    pst.setString(5, username);
                    pst.setString(6, password);

                    if(pst.executeUpdate() > 0){
                    JOptionPane.showMessageDialog(null, "SignUp Success! Please Login!");

                    this.setVisible(false);
                    Login login = new Login();
                    login.pack();
                    login.setLocationRelativeTo(null);
                    login.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null, "SignUp Failed!");
                    }

                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "SignUp Failed!");
                    Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSignUpMouseClicked
    
    private boolean checkExistingUsername(String username){
        
        String query = "SELECT * FROM Account Where username = ?";
        try {
            PreparedStatement pst = SQLConnection.getConnection().prepareStatement(query);
            pst.setString(1, username);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                return true;
            }  
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
    
